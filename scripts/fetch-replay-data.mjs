// Fetches REAL daily close prices for each Replay "era" from the Coinbase
// Exchange API (free, no key, history back to ~2015 for BTC / ~2016 for ETH)
// and writes them to src/data/replayHistory.ts so the Replay feature plays back
// actual history instead of a synthetic seeded series.
//
//   npm run fetch:replay
//
// Re-run any time to refresh. The output is committed so the app ships offline.

import { writeFileSync } from 'node:fs';

// Each era: a real date range + the coin whose history we replay. Ranges are
// chosen to capture the story (pre-move → move → aftermath) at daily resolution.
const ERAS = [
  { id: 'bull-run-2021',      title: 'The 2021 Bull Run', dateLabel: 'Nov 2020 → May 2021', coin: 'BTC', product: 'BTC-USD', start: '2020-11-01', end: '2021-05-01' },
  { id: 'crypto-winter-2022', title: 'Crypto Winter 2022', dateLabel: 'May → Nov 2022 · LUNA + FTX', coin: 'BTC', product: 'BTC-USD', start: '2022-05-01', end: '2022-11-13' },
  { id: 'defi-summer',        title: 'DeFi Summer',       dateLabel: 'May → Sep 2020', coin: 'ETH', product: 'ETH-USD', start: '2020-05-01', end: '2020-09-30' },
  { id: 'covid-crash',        title: 'COVID Crash',       dateLabel: 'Feb → Apr 2020', coin: 'BTC', product: 'BTC-USD', start: '2020-02-15', end: '2020-04-15' },
  { id: 'ico-boom-2017',      title: '2017 ICO Boom',     dateLabel: 'Jul → Dec 2017', coin: 'BTC', product: 'BTC-USD', start: '2017-07-01', end: '2017-12-31' },
];

const DAY = 86400 * 1000;
const sleep = ms => new Promise(r => setTimeout(r, ms));

// Coinbase returns at most 300 candles per request, so walk the range in
// <=290-day windows. Each candle is [time(sec), low, high, open, close, volume].
async function fetchCloses(product, startISO, endISO) {
  const startMs = Date.parse(startISO);
  const endMs = Date.parse(endISO);
  const byDay = new Map(); // day-key (sec) -> close, deduped
  for (let from = startMs; from < endMs; from += 290 * DAY) {
    const to = Math.min(from + 290 * DAY, endMs);
    const url = `https://api.exchange.coinbase.com/products/${product}/candles`
      + `?granularity=86400&start=${new Date(from).toISOString()}&end=${new Date(to).toISOString()}`;
    let rows;
    for (let attempt = 0; attempt < 4; attempt++) {
      const res = await fetch(url, { headers: { 'User-Agent': 'crypto-sim-replay-fetch' } });
      if (res.ok) { rows = await res.json(); break; }
      await sleep(800 * (attempt + 1));
    }
    if (!Array.isArray(rows)) throw new Error(`Failed to fetch ${product} ${startISO}..${endISO}`);
    for (const r of rows) byDay.set(r[0], r[4]); // time -> close
    await sleep(250); // be polite to the public endpoint
  }
  return [...byDay.entries()]
    .sort((a, b) => a[0] - b[0])
    .map(([, close]) => Math.round(close * 100) / 100);
}

const out = [];
for (const e of ERAS) {
  process.stdout.write(`Fetching ${e.id} (${e.product} ${e.start}..${e.end})… `);
  const prices = await fetchCloses(e.product, e.start, e.end);
  if (prices.length < 5) throw new Error(`${e.id}: only ${prices.length} points`);
  const pct = ((prices[prices.length - 1] - prices[0]) / prices[0]) * 100;
  console.log(`${prices.length} days, $${prices[0].toLocaleString()} → $${prices[prices.length - 1].toLocaleString()} (${pct >= 0 ? '+' : ''}${pct.toFixed(0)}%)`);
  out.push({ id: e.id, title: e.title, dateLabel: e.dateLabel, coin: e.coin, startDate: e.start, intervalMs: DAY, prices });
}

const body = `// AUTO-GENERATED by scripts/fetch-replay-data.mjs — do not edit by hand.
// Real daily close prices (Coinbase) powering the Replay "time machine".
// Re-generate with: npm run fetch:replay

export interface ReplayEra {
  id: string;
  title: string;
  dateLabel: string;
  coin: string;
  /** ISO date of prices[0] — lets the UI show the real calendar date per step. */
  startDate: string;
  /** Milliseconds each price step represents (one day for now). */
  intervalMs: number;
  /** Real close prices, oldest → newest. One point per interval. */
  prices: number[];
}

export const REPLAY_ERAS: ReplayEra[] = ${JSON.stringify(out, null, 2)};

export type ReplayEraId = ${out.map(e => `'${e.id}'`).join(' | ')};
`;

writeFileSync(new URL('../src/data/replayHistory.ts', import.meta.url), body);
console.log(`\nWrote src/data/replayHistory.ts (${out.length} eras)`);
