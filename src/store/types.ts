export interface Coin {
  symbol: string;
  name: string;
  price: number;
  change24h: number;
  marketCap: string;
  volume: string;
  history: number[];    // real 24h price series (hourly sparkline), refreshed each
                        // poll; graphs append the live price as the right-edge tip
  high24h?: number;     // raw USD from CoinGecko, undefined until first UPDATE_PRICES
  low24h?: number;
}

export interface Holding {
  symbol: string;
  units: number;
  avgCost: number;
}

export interface Trade {
  id: string;
  symbol: string;
  side: 'buy' | 'sell';
  amount: number;
  units: number;
  price: number;
  timestamp: number;
  xpEarned: number;
  slippage: number;
  // 'reward' = a cash-injection event (e.g. daily-reward bonus), recorded with
  // symbol 'USD' (the CASH_EVENT_SYMBOL sentinel), units 0, amount = bonus. It
  // is not a coin trade; ledger replay treats symbol 'USD' as a pure cash delta.
  // 'rebalance' = a coin buy/sell generated by the Rebalance action (vs a manual
  // trade), so the history can mark it "(rebalanced)". Persists for the offline
  // portfolio (local store); cloud rows have no `kind` column.
  // Optional/back-compat: undefined on coin trades and on rows reloaded from the
  // cloud (the Trade model has no `kind` column — the symbol carries the mark).
  kind?: 'trade' | 'reward' | 'rebalance';
  // Realized P&L on a sell, in dollars (proceeds − cost basis at avgCost). Set
  // when the sell executes; undefined on buys/older rows.
  realizedPnl?: number;
}

export interface Tournament {
  id: string;
  name: string;
  type: 'daily' | 'featured' | 'replay' | '1v1';
  status: 'live' | 'open' | 'finished';
  prizePool: string;
  players: number;
  userRank: number;
  endsAt: number;
  stake: string;
}

export interface Competition {
  id: string;
  name: string;
  type: 'daily' | 'featured' | 'replay' | '1v1';
  status: 'open' | 'live' | 'finished';
  prizePool: string;
  maxPlayers: number;
  stake: string;
  startAt: number;
  endAt: number;
  entryCount: number;
  numberOfPrizes: number;   // count of paid positions
  prizes: number[];         // dollar amount per rank (index 0 = #1)
  prizeXp: number;          // winner's XP award when cash prizes are off (podium splits it)
  inviteCode?: string;      // 1v1 duels only — shareable join code
  challengerHandle?: string; // 1v1 duels only — who created the duel
  // When true, no new entries are accepted once the contest has started
  // (now >= startAt). Default false = players can still join live.
  lockAfterStart?: boolean;
  // Fraction of elapsed time (0..1) after which joining closes — e.g. 0.9 means
  // "joinable until only 10% of the duration remains". Undefined = joinable until
  // the contest ends. See isJoinLocked() in competitionService.
  joinCutoffPct?: number;
  // True for real-money cash-prize contests. Payments-off builds filter these
  // out (query + client guard) so they never appear there. Legacy/XP contests
  // are false/undefined and show on every build.
  cashPrize?: boolean;
}

